public class OperatorDemo{
	public static void main(String[] args){
		int i = 10;
		System.out.println(i++); //prints i =10 & increments by 1 , i = 10
		System.out.println(i--);// prints i = 11 & then decrements by 1 so i = 10
		System.out.println(++i);//increments i(11) & then prints, so i = 11
		System.out.println(--i);//decrements i & then prints so i = 10
	
	}
}