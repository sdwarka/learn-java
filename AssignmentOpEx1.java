public class AssignmentOpEx1{
	public static void main(String[] args){
		int a = 10;
		System.out.println("a = " +a);
		a+=3;//10+3
		System.out.println("a+=3 " +a);
		a-=4;//13-4
		System.out.println("a-=4 " +a);
		a*=2;//9*2
		System.out.println("a*=2 " +a);
		a/=2;//18/2
		System.out.println("a/=2 " +a);
		a%=2;
		System.out.println("a%=2 " +a);
	}
}